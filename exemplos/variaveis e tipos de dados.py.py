# Exemplo 2: Variáveis e Tipos de Dados em Python
# Python possui tipagem dinâmica, ou seja, você não precisa declarar o tipo da variável explicitamente.

# Texto (String)
nome_linguagem = "Python"

# Inteiro (Integer)
ano_criacao = 1991

# Decimal (Float)
versao_atual = 3.12

# Booleano (Boolean)
eh_facil_aprender = True

# Exibindo os valores e seus respectivos tipos na tela
print(f"Linguagem: {nome_linguagem} | Tipo: {type(nome_linguagem)}")
print(f"Ano de Criação: {ano_criacao} | Tipo: {type(ano_criacao)}")
print(f"Versão: {versao_atual} | Tipo: {type(versao_atual)}")
print(f"É fácil aprender? {eh_facil_aprender} | Tipo: {type(eh_facil_aprender)}")
